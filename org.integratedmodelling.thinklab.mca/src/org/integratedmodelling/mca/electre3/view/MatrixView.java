    public void setModel(MatrixModel cm) {
        concordances = new TwoHeadersTableModel();
        Object[] alternatives = parent.getDisplayedAlternatives();

        int rows = alternatives.length + 1;
        String[][] data = new String[rows][rows];
        data[0][0] = "";
        int i = 1;
        concordances.addColumn(null);
        for (int ai = 0; ai < alternatives.length; ai++) {
            Alternative a = (Alternative) (alternatives[ai]);
            data[0][i++] = a.getName();
            concordances.addColumn(a);
        }
        concordances.addRow(data[0]);

        i = 0;
        for (int ai1 = 0; ai1 < alternatives.length; ai1++) {
            Alternative a1 = (Alternative) (alternatives[ai1]);
            data[++i][0] = a1.getName();
            int j = 1;
            for (int ai2 = 0; ai2 < alternatives.length; ai2++) {
                Alternative a2 = (Alternative) (alternatives[ai2]);
                AAPair aa = new AAPair(a1, a2);
                double perf = cm.getValue(aa);
                data[i][j++] = ViewSettings.getDecimalFormat().format(perf);
            }
            concordances.addRow(data[i]);
        }
        tblConcordanceMatrix.setModel(concordances);
    }

